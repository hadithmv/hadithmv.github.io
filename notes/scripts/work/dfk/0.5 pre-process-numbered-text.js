function addAsterisksToNumbers(text) {
  // This regex looks for a line that starts with optional whitespace,
  // followed by one or more digits, then a hyphen
  return text.replace(/^(\s*)(\d+)(-)/gm, "$1**$2$3");
}

// Example usage
const fs = require("fs");

// Read the file
fs.readFile("pre-process-numbered-text.txt", "utf8", (err, data) => {
  if (err) {
    console.error("Error reading the file:", err);
    return;
  }

  // Process the text
  const processedText = addAsterisksToNumbers(data);

  // Write the result to a new file or overwrite the existing one
  fs.writeFile("processed-numbered-text.txt", processedText, "utf8", (err) => {
    if (err) {
      console.error("Error writing to the file:", err);
      return;
    }
    console.log("Processing complete. Check processed-numbered-text.txt");
  });
});

